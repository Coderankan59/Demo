// Addition of the two integer numbers
#include<stdio.h>
int main()
{
	int x,y,addition;
	printf("Addition of the two integer numbers\n");
	printf("Enter the first integer number: ");
	scanf("%d",&x);
	printf("Enter the second integer number: ");
	scanf("%d",&y);
	addition=x+y;
	printf("The Addition of the %d and %d is: %d",x,y,addition);
	return 0;
}

